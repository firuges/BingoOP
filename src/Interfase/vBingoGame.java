    public boolean checkReadyAll(String cantidad){
        boolean bandera = true;
        int cant = Integer.parseInt(cantidad);
        int contador = 0;
        for(cJugador u: observer.getElJuego().getJugadores()){
            if(!u.isReady()){
                bandera = false;
            }
            contador ++;
        }
        if(cant == contador && bandera == true){
            return bandera;
        }
        return false;
    }
